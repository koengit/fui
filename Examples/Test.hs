module Main where

----------------------------------------------------------------------------

import Graphics.UI.Fui

----------------------------------------------------------------------------

main :: IO ()
main = run hello

----------------------------------------------------------------------------

hello :: GUI
hello =
   channel $ \name ->
   window [ text <+ val "Hej" ]
   ( label  [ text <+ source name ]
 .^^ entry  [ text +> sink name ]
 .^^ button [ text <+ val "Hello" ]
   )

